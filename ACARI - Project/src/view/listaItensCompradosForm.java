    private void excluirItemCompradoListaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirItemCompradoListaButtonActionPerformed
        //Pega a tabela e transforma num modelo o qual podemos manipular seu conteudo
        modeloTabelaItensComprados model = (modeloTabelaItensComprados) jTableItensComprados.getModel();
        //Validamos o resultado
        if (jTableItensComprados.getSelectedRow() != -1) {
            //Pegamos os dados selecionados na tabela e encontramos o modelo na lista de associados
            
            principalControlador.getControladorCompras().removeItem(
                    id_compra_master,
                    model.getLinhas().get(jTableItensComprados.getSelectedRow()).getId_material()
            );
        } else {
            JOptionPane.showMessageDialog(null, "Favor selecionar uma empresa na tabela primeiro!");
        }
        preencherTabela();
    }//GEN-LAST:event_excluirItemCompradoListaButtonActionPerformed

    public void preencherTabela() {
        JTable table = jTableItensComprados;
        modeloTabelaItensComprados modeloIC = null;

        modeloIC = new modeloTabelaItensComprados(principalControlador.getControladorCompras().getItensFromCompraID(id_compra_master), principalControlador);
        if (modeloIC != null) {
            table.setModel(modeloIC);
            table.getTableHeader().setReorderingAllowed(false);
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao procurar a tabela para preenchimento!!!", "ERROR!", JOptionPane.ERROR_MESSAGE);
        }
    }


