    private void editarItemCompradoListaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarItemCompradoListaButtonActionPerformed
        //Pega a tabela e transforma num modelo o qual podemos manipular seu conteudo
        modeloTabelaItensComprados model = (modeloTabelaItensComprados) jTableItensComprados.getModel();
        //Validamos o resultado
        if (jTableItensComprados.getSelectedRow() != -1) {
            //Pegamos os dados selecionados na tabela e encontramos o modelo na lista de associados
            ItemComprado item = principalControlador.getControladorCompras().getEspecificItem(
                    id_compra_master, model.getLinhas().get(jTableItensComprados.getSelectedRow()).getId_material()
            );
            if (item != null) {
                new fichaItemCompradoForm(principalControlador, item, id_compra_master, this);//iniciamos a tela enviando um modelo
            }
        } else {
            JOptionPane.showMessageDialog(null, "Favor selecionar um associado na tabela primeiro!");
        }
    }//GEN-LAST:event_editarItemCompradoListaButtonActionPerformed

